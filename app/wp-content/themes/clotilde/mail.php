            $errors[] = $field;
        } else {
            $_POST[$field] = mysql_real_escape_string($_POST[$field]);
        }
    }

    if (count($errors) > 0) {

        echo json_encode(array('status' => "errors", 'errors' => $errors));

    } else {
        $admin_email = get_option( 'admin_email' );
